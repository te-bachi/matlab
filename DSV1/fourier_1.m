
clear all;
close all;
clc;

t = 0:0.01:20;
y =   1  * sin(t)      + ...
    1/3  * sin(3  * t) + ...
    1/5  * sin(5  * t) + ...
    1/7  * sin(7  * t) + ...
    1/9  * sin(9  * t) + ...
    1/11 * sin(11 * t) + ...
    1/13 * sin(13 * t) + ...
    1/15 * sin(15 * t) + ...
    1/17 * sin(17 * t) + ...
    1/19 * sin(19 * t) + ...
    1/21 * sin(21 * t);

plot(t, y);
grid on;
grid minor;
