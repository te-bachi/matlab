
clear;
clc;

%t = [  2,  4,  6,  8,  10 ];
%T = [ 40, 20, 10,  6,   4 ];

t = [  1,  2,  3,  4,   5 ];
T = [ 10, 30,  5, 15,  25 ];

l04 = @(x) ((x    - t(2)).*(x    - t(3)).*(x    - t(4)).*(x    - t(5))) / ...
           ((t(1) - t(2)).*(t(1) - t(3)).*(t(1) - t(4)).*(t(1) - t(5)));
       
l14 = @(x) ((x    - t(1)).*(x    - t(3)).*(x    - t(4)).*(x    - t(5))) / ...
           ((t(2) - t(1)).*(t(2) - t(3)).*(t(2) - t(4)).*(t(2) - t(5)));

l24 = @(x) ((x    - t(1)).*(x    - t(2)).*(x    - t(4)).*(x    - t(5))) / ...
           ((t(3) - t(1)).*(t(3) - t(2)).*(t(3) - t(4)).*(t(3) - t(5)));

l34 = @(x) ((x    - t(1)).*(x    - t(2)).*(x    - t(3)).*(x    - t(5))) / ...
           ((t(4) - t(1)).*(t(4) - t(2)).*(t(4) - t(3)).*(t(4) - t(5)));

l44 = @(x) ((x    - t(1)).*(x    - t(2)).*(x    - t(3)).*(x    - t(4))) / ...
           ((t(5) - t(1)).*(t(5) - t(2)).*(t(5) - t(3)).*(t(5) - t(4)));

p   = @(x) T(1)*l04(x) + T(2)*l14(x) + T(3)*l24(x) + T(4)*l34(x) + T(5)*l44(x);

x = linspace(t(1),t(end));
plot(x, p(x));
hold on;
scatter(t, T);

